export const neighborhood = (value1, value2) => {
    const neighborhoodData = []; 

    let start = false;

    for(let nei of neighborhoodInfo){
        if(nei[0] === value1 && nei[1] === value2 ){
            start = true;
            neighborhoodData.push({
                value: nei[2], 
                text: nei[2]
            });
        }
        else {
            if(start===true) break;
        }
    }

    return neighborhoodData;
} 

